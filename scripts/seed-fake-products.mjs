// ── Seed ─────────────────────────────────────────────────────────────────────
async function main() {
  console.log(`⏳ Création de ${products.length} produits…`);
  let created = 0;
  let skipped = 0;

  for (const p of products) {
    const baseSlug = slug(p.name);
    const existing = await prisma.product.findUnique({
      where: { slug: baseSlug },
    });
    if (existing) {
      skipped++;
      continue;
    }

    await prisma.product.create({
      data: {
        name: p.name,
        slug: baseSlug,
        description: p.desc,
        price: p.price,
        salePrice: p.salePrice ?? null,
        stock: p.stock,
        status: "ACTIVE",
        isNew: p.isNew,
        color: p.color ?? null,
        images: IMG[
          p.cat === CAT.acierInox ||
          p.cat === CAT.plaqueOr ||
          p.cat === CAT.kitsBijoux ||
          p.cat === CAT.perlerie
            ? "bijou"
            : p.cat === CAT.artsTable ||
                p.cat === CAT.bougies ||
                p.cat === CAT.decoMurale
              ? "deco"
              : p.cat === CAT.papeterie ||
                  p.cat === CAT.aquarelle ||
                  p.cat === CAT.pastels
                ? "papier"
                : p.cat === CAT.puzzles ||
                    p.cat === CAT.jeux ||
                    p.cat === CAT.peintureNumero ||
                    p.cat === CAT.diamondPainting ||
                    p.cat === CAT.kitsDiy
                  ? "jeux"
                  : "lifestyle"
        ](baseSlug.slice(0, 6)),
        categoryId: p.cat,
        brandId: p.brand,
      },
    });
    created++;
    process.stdout.write(`\r  ✔ ${created} créés`);
  }

  console.log(`\n✅ Terminé : ${created} créés, ${skipped} déjà existants.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
